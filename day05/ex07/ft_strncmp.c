int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int index;
	int x;
	x = n;
	index = 0;
	while (--x >= 0 && (s1[index] != '\0' || s2[index] != '\0'))
	{
		if (s1[index] == s2[index])
			index++;
		else
			return (s1[index] - s2[index]);
	}
	return (0);
}
