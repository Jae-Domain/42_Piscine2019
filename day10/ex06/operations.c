int		do_add(int x, int y)
{
	return (x + y);
}

int		do_sub(int x, int y)
{
	return (x - y);
}

int		do_div(int x, int y)
{
	return (x / y);
}

int		do_mul(int x, int y)
{
	return (x * y);
}

int		do_mod(int x, int y)
{
	return (x % y);
}
