int		ft_sqrt(int nb)
{
	int i;

	if (nb == 0 || nb == 1)
	{
		return (nb);
	}
	i = 2;
	while (i++ <= nb / 2)
	{
		if (i == nb / i && nb % i == 0)
		{
			return (i);
		}
	}
	return (0);
}
